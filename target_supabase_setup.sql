-- 10. OPTIMIZATIONS & VIEWS
CREATE OR REPLACE VIEW latest_device_telemetry AS
SELECT DISTINCT ON (boat_id, mac_address) *
FROM telemetry
ORDER BY boat_id, mac_address, created_at DESC;

CREATE OR REPLACE FUNCTION get_daily_max_values(p_boat_id UUID) 
RETURNS TABLE (
  mac_address TEXT, 
  pmax NUMERIC, 
  vmax NUMERIC
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    t.mac_address::text,
    MAX(t.pv_power)::numeric as pmax,
    MAX(t.pv_voltage)::numeric as vmax
  FROM telemetry t
  WHERE t.boat_id = p_boat_id
    AND t.created_at >= date_trunc('day', CURRENT_DATE)
    AND t.device_type = 1
  GROUP BY t.mac_address;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 11. INGEST FUNCTION
CREATE OR REPLACE FUNCTION ingest_telemetry(payload jsonb)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_boat_id uuid;
  v_count int := 0;
  item jsonb;
BEGIN
  SELECT id INTO v_boat_id 
  FROM boats 
  WHERE id = (payload->>'boat_id')::uuid 
    AND device_secret = payload->>'device_secret';
    
  IF v_boat_id IS NULL THEN
    RETURN jsonb_build_object('success', false, 'error', 'Invalid boat_id or secret');
  END IF;

  FOR item IN SELECT * FROM jsonb_array_elements(payload->'data')
  LOOP
    IF item->>'mac_address' IS NOT NULL THEN
        INSERT INTO telemetry (
            boat_id,
            mac_address,
            voltage,
            current,
            temperature,
            alarm,
            device_type,
            soc,
            pv_power,
            pv_voltage,
            pv_current,
            load_current,
            device_state,
            yield_today,
            efficiency,
            consumed_ah,
            remaining_mins,
            aux_voltage,
            load_state
        ) VALUES (
            v_boat_id,
            item->>'mac_address',
            (item->>'voltage')::numeric,
            (item->>'current')::numeric,
            (item->>'temperature')::numeric,
            (item->>'alarm')::int,
            (item->>'device_type')::int,
            (item->>'soc')::numeric,
            (item->>'pv_power')::numeric,
            (item->>'pv_voltage')::numeric,
            (item->>'pv_current')::numeric,
            (item->>'load_current')::numeric,
            (item->>'device_state')::int,
            (item->>'yield_today')::numeric,
            (item->>'efficiency')::numeric,
            (item->>'consumed_ah')::numeric,
            (item->>'remaining_mins')::numeric,
            (item->>'aux_voltage')::numeric,
            (item->>'load_state')::int
        );
        v_count := v_count + 1;
    END IF;
  END LOOP;

  RETURN jsonb_build_object('success', true, 'inserted_count', v_count);
END;
$$;

