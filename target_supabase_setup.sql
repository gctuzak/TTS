-- 2. BOATS TABLE
create table if not exists boats (
  id uuid primary key default uuid_generate_v4(),
  user_id uuid references auth.users not null,
  name text not null,
  device_secret text not null,
  mac_address text,
  created_at timestamptz default now()
);

-- 3. TELEMETRY TABLE
create table if not exists telemetry (
  id bigint generated by default as identity primary key,
  boat_id uuid references boats(id) not null,
  created_at timestamptz default now(),
  
  -- Common Data
  voltage numeric,
  current numeric,
  power numeric,
  
  -- Battery Monitor
  soc numeric,
  consumed_ah numeric,
  remaining_mins integer,
  aux_voltage numeric,
  
  -- Solar Charger
  pv_power numeric,
  pv_voltage numeric,
  pv_current numeric,
  load_current numeric,
  load_state integer,
  device_state integer,
  yield_today numeric,
  efficiency numeric,
  temperature numeric,
  alarm integer,
  mac_address text,
  device_type integer,
  charge_state integer,
  total_yield numeric,
  max_pv_voltage numeric,
  max_pv_power numeric,
  min_battery_voltage numeric,
  max_battery_voltage numeric
);

